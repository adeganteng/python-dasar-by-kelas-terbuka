# Variabel adalah tempat untuk menyimpan data

# A. Menaruh / Assignment nilai
a = 10
""" 
"a" => Variabel
"=" => Assignment
"10" => Value
"""
x = 5
panjang = 1000

# Pemanggilan pertama
print(a) # 10
print("Nilai a = ", a) # Nilai a =  10
print("Nilai x = ", x) # Nilai x =  5
print("Nilai panjang = ", panjang) # Nilai panjang =  1000


